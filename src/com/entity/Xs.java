package com.entity;

import java.util.Date;

/*学生实体类*/
public class Xs {
		
	private int xsno;
	private String xsname;
	private int xsage;
	private String xssex;
	private String xsshobby;
	private String xsimg;
	private Date xssintime;
	
	
	
	
	
	
	
	

	public Xs() {
		super();
	}









	public Xs(int xsno, String xsname, int xsage, String xssex,
			String xsshobby, String xsimg, Date xssintime) {
		super();
		this.xsno = xsno;
		this.xsname = xsname;
		this.xsage = xsage;
		this.xssex = xssex;
		this.xsshobby = xsshobby;
		this.xsimg = xsimg;
		this.xssintime = xssintime;
	}









	public int getXsno() {
		return xsno;
	}









	public void setXsno(int xsno) {
		this.xsno = xsno;
	}









	public String getXsname() {
		return xsname;
	}









	public void setXsname(String xsname) {
		this.xsname = xsname;
	}









	public int getXsage() {
		return xsage;
	}









	public void setXsage(int xsage) {
		this.xsage = xsage;
	}









	public String getXssex() {
		return xssex;
	}









	public void setXssex(String xssex) {
		this.xssex = xssex;
	}









	public String getXsshobby() {
		return xsshobby;
	}









	public void setXsshobby(String xsshobby) {
		this.xsshobby = xsshobby;
	}









	public String getXsimg() {
		return xsimg;
	}









	public void setXsimg(String xsimg) {
		this.xsimg = xsimg;
	}









	public Date getXssintime() {
		return xssintime;
	}









	public void setXssintime(Date xssintime) {
		this.xssintime = xssintime;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
